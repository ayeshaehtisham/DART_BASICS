 // This program will print numbers 1 to 10 and 10 to 1 using for Loop:

 void main() {
  // To print 1 to 10 using for Loop:
  for ( int i = 1; i <= 10; i++ ) {
    print(i);
  }

  print("----------------");
  
  // To print 10 to 1 using for Loop:
  for ( int i = 10; i >= 1; i-- ) {
    print(i);
  }
 }
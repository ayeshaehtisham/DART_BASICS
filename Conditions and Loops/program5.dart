 // Break and continue in Dart:

 void main() {

  // Break in Dart For Loop:
  for (int i = 1; i <= 10; i++) {
    if (i == 5) {
      break;
    }
    print(i);
  }

  print("---------------");

  // Continue In Dart:
  for (int i = 1; i <= 10; i++) {
    if (i == 5) {
      continue;
    }
    print(i);
  }
}